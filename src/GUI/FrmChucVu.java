    private void rankDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rankDeleteBtnActionPerformed
        // TODO add your handling code here:
        int row = rankTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn chức vụ cần xóa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không ?");
            
            if (confirm == JOptionPane.YES_OPTION) {
                String rankCode = String.valueOf(rankTable.getValueAt(row, 0));
                
                cvbus.deleteChucVu(rankCode);
                defaultTableModel.setRowCount(0);
                setCVData(cvbus.getChucVu());
            }
        }
    }//GEN-LAST:event_rankDeleteBtnActionPerformed

    private void rankSaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rankSaveBtnActionPerformed
        // TODO add your handling code here:
        int row = rankTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn chức vụ cần sửa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            cvdto.setMaCV(rankIDField.getText());
            cvdto.setTenCV(rankNameField.getText());
            cvdto.setGhiChu(noteField.getText());

            cvbus.updateChucVu(cvdto);
            JOptionPane.showMessageDialog(this, "Sửa chức vụ thành công !!!");
            defaultTableModel.setRowCount(0);
            setCVData(cvbus.getChucVu());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_rankSaveBtnActionPerformed

