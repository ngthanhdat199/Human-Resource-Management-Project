    private void rankAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rankAddBtnActionPerformed
        // TODO add your handling code here:
        if("".equals(rankIDField.getText()) || "".equals(rankNameField.getText())) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống dữ liệu nhập vào !!!","Lỗi",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            cvdto.setMaCV(rankIDField.getText());
            cvdto.setTenCV(rankNameField.getText());
            cvdto.setGhiChu(noteField.getText());
            cvbus.addChucVu(cvdto);
            JOptionPane.showMessageDialog(this, "Thêm chức vụ thành công !!!");
            defaultTableModel.setRowCount(0);
            setCVData(cvbus.getChucVu());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);

        }
    }//GEN-LAST:event_rankAddBtnActionPerformed

