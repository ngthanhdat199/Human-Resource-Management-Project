    public void showInfo() {
        int row = rankTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn chức vụ cần sửa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }else {
            String rankCode = String.valueOf(rankTable.getValueAt(row, 0));
            cvdto = cvbus.findCVByID(rankCode);
            rankIDField.setText(cvdto.getMaCV());
            rankIDField.setEnabled(false);
            rankNameField.setText(cvdto.getTenCV());
            noteField.setText(cvdto.getGhiChu());
        }
    }

